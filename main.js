// –ü—Ä–∏–º–µ–Ω–µ–Ω–∏–µ —Ñ–∏–ª—å—Ç—Ä–æ–≤ –∏ –ø–æ–∏—Å–∫–∞
function applyFiltersAndSearch() {
    const searchInput = document.getElementById('catalogSearch');
    const searchTerm = searchInput ? searchInput.value.toLowerCase().trim() : '';
    const cards = document.querySelectorAll('#catalogProducts .product-card');
    
    console.log('–ü—Ä–∏–º–µ–Ω–µ–Ω–∏–µ —Ñ–∏–ª—å—Ç—Ä–æ–≤. –§–∏–ª—å—Ç—Ä:', currentFilter, '–ü–æ–∏—Å–∫:', searchTerm);
    console.log('–í—Å–µ–≥–æ –∫–∞—Ä—Ç–æ—á–µ–∫:', cards.length);
    
    let visibleCount = 0;
    
    cards.forEach(card => {
        const category = card.dataset.category;
        const title = card.querySelector('.product-card__title').textContent.toLowerCase();
        const categoryName = card.querySelector('.product-card__category').textContent.toLowerCase();
        
        // –ü—Ä–æ–≤–µ—Ä–∫–∞ —Ñ–∏–ª—å—Ç—Ä–∞
        const matchesFilter = currentFilter === 'all' || category === currentFilter;
        
        // –ü—Ä–æ–≤–µ—Ä–∫–∞ –ø–æ–∏—Å–∫–∞
        const matchesSearch = searchTerm === '' || 
                            title.includes(searchTerm) || 
                            categoryName.includes(searchTerm);
        
        if (matchesFilter && matchesSearch) {
            card.style.display = 'block';
            visibleCount++;
        } else {
            card.style.display = 'none';
        }
    });
    
    console.log('–í–∏–¥–∏–º—ã—Ö –∫–∞—Ä—Ç–æ—á–µ–∫:', visibleCount);
    updateSearchResults(visibleCount, searchTerm);
}

