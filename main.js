function changeQuantity(index, change) {
    const item = cart[index];
    if (item.type === 'buy') {
        item.quantity += change;
        if (item.quantity < 1) item.quantity = 1;
        item.price = item.pricePerUnit * item.quantity;
        renderCart();
    }
}

function removeFromCart(index) {
    cart.splice(index, 1);
    updateCartCount();
}

