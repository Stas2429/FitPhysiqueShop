function initNewsSlider() {
    const slider = document.getElementById('newsSlider');
    
    if (!slider) return;
    
    const newsCards = slider.querySelectorAll('.news-card');
    newsCardsCount = newsCards.length;
    
    // –°–±—Ä–æ—Å —Ç–µ–∫—É—â–µ–≥–æ —Å–ª–∞–π–¥–∞ –µ—Å–ª–∏ –æ–Ω –≤—ã—Ö–æ–¥–∏—Ç –∑–∞ –ø—Ä–µ–¥–µ–ª—ã
    if (currentNewsSlide >= newsCardsCount) {
        currentNewsSlide = 0;
    }
}

function slideNews(direction) {
    if (newsCardsCount === 0) return;

    const width = window.innerWidth;
    let visibleCards;

    if (width <= 768) visibleCards = 1;
    else if (width <= 1024) visibleCards = 2;
    else visibleCards = 3;

    const maxSlideIndex = Math.max(0, newsCardsCount - visibleCards);

    currentNewsSlide += direction;

    // –ó–∞—Ü–∏–∫–ª–∏–≤–∞–Ω–∏–µ
    if (currentNewsSlide < 0) {
        currentNewsSlide = maxSlideIndex;
    } else if (currentNewsSlide > maxSlideIndex) {
        currentNewsSlide = 0;
    }

    updateNewsSlider();
}

function updateNewsSlider() {
    const slider = document.getElementById('newsSlider');
    
    if (!slider) return;
    
    const newsCards = slider.querySelectorAll('.news-card');
    if (newsCards.length === 0) return;
    
    // –û–ø—Ä–µ–¥–µ–ª—è–µ–º –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –≤–∏–¥–∏–º—ã—Ö –∫–∞—Ä—Ç–æ—á–µ–∫ –≤ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ –æ—Ç —à–∏—Ä–∏–Ω—ã —ç–∫—Ä–∞–Ω–∞
    const width = window.innerWidth;
    let visibleCards;
    
    if (width <= 768) {
        visibleCards = 1;
    } else if (width <= 1024) {
        visibleCards = 2;
    } else {
        visibleCards = 3;
    }
    const maxSlideIndex = Math.max(0, newsCardsCount - visibleCards);

    // –í—ã—á–∏—Å–ª—è–µ–º —Å–¥–≤–∏–≥ –¥–ª—è —Ü–µ–Ω—Ç—Ä–∏—Ä–æ–≤–∞–Ω–∏—è
    const cardWidth = newsCards[0].offsetWidth;
    const gap = parseFloat(getComputedStyle(slider).gap) || 32;
    
    // –°–º–µ—â–µ–Ω–∏–µ –¥–ª—è –æ–¥–Ω–æ–π –∫–∞—Ä—Ç–æ—á–∫–∏
    const offset = currentNewsSlide * (cardWidth + gap);
    
    // –î–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω–æ–µ —Å–º–µ—â–µ–Ω–∏–µ –¥–ª—è —Ü–µ–Ω—Ç—Ä–∏—Ä–æ–≤–∞–Ω–∏—è –ø—Ä–∏ –Ω–µ—á—ë—Ç–Ω–æ–º –∫–æ–ª–∏—á–µ—Å—Ç–≤–µ –≤–∏–¥–∏–º—ã—Ö –∫–∞—Ä—Ç–æ—á–µ–∫
    const wrapperWidth = slider.parentElement.offsetWidth;
    const totalVisibleWidth = (cardWidth * visibleCards) + (gap * (visibleCards - 1));
    const centerOffset = (wrapperWidth - totalVisibleWidth) / 2;
    
    slider.style.transform = `translateX(${centerOffset - offset}px)`;
}

// Swipe –¥–ª—è –º–æ–±–∏–ª—å–Ω—ã—Ö —É—Å—Ç—Ä–æ–π—Å—Ç–≤
let touchStartX = 0;
let touchEndX = 0;

document.addEventListener('DOMContentLoaded', function() {
    const newsSlider = document.getElementById('newsSlider');
    
    if (newsSlider) {
        newsSlider.addEventListener('touchstart', (e) => {
            touchStartX = e.changedTouches[0].screenX;
        }, { passive: true });
        
        newsSlider.addEventListener('touchend', (e) => {
            touchEndX = e.changedTouches[0].screenX;
            handleNewsSwipe();
        }, { passive: true });
    }
});

function handleNewsSwipe() {
    const swipeThreshold = 50;
    
    if (touchEndX < touchStartX - swipeThreshold) {
        slideNews(1);
    }
    
    if (touchEndX > touchStartX + swipeThreshold) {
        slideNews(-1);
    }
}

